export function localize(hass, key, vars = {}, fallback) {
  const lang = (hass?.language || hass?.selectedLanguage || 'en').toLowerCase();
  const code = lang.split('-')[0];
  const dict = STRINGS[code] || STRINGS.en;
  const template = dict[key] ?? STRINGS.en[key] ?? fallback ?? key;
  return template.replace(/\{(\w+)\}/g, (_, token) => {
    const val = vars[token];
    return val === undefined || val === null ? `{${token}}` : String(val);
  });
}
